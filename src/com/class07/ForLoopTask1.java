package com.class07;

public class ForLoopTask1 {

	public static void main(String[] args) {
		
		//print numbers from 1 to 100
		for (int a=1; a<=100; a++) {
			System.out.println(a);
		}
		//Print numbers from 100 to 1
		for (int c=100; c>=1; c--) {
			System.out.println(c);
		}
		//print odd numbers from 1 to 20 (first  way-for loop)
		//print odd numbers from 1 to 20 (second  way-do while)
		for (int d=1;  d<=20;  d++) {
			if (d%2!=0) {
			System.out.println(d);	
			}
			
		//print even numbers from 20 to 1 (first  way-for loop)
		//print even numbers from 20 to 1 (second  way-do while)
		for (int e=20;  e>=1;  e--)	{
			if (e%2==0) {
			System.out.println(e);
			}
		//print even numbers between 20 and 50 (first  way)
		//print even numbers between 20 and 50 (second  way)
		for (int f=20;  f<=50;  f++) {
			if (f%2==0) {
			System.out.println(f);	
			}
		
		
		//print odd numbers between 20 and 50 (first  way-for loop)
		//print odd numbers between 20 and 50 (second  way-do while)
		for (int g=20;  g<=50;  g++) {
			if (g%2!=0) {
			System.out.println(g);
		}
		}
		}
		
		}
		}
	    }

        }
